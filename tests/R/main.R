#' Add two numbers together.
#'
#' A simple function that computes the sum of two numeric inputs.
#'
#' @param x A numeric value.
#' @param y A numeric value.
#'
#' @returns A numeric value, the sum of `x` and `y`.
#'
#' @examples
#' test_function(1, 2)
#' test_function(-5, 10)
add <- function(x, y) {
  x + y
}

#' Subtract two numbers.
#'
#' A simple function that subtracts two numeric inputs. It will
#' add them using the following rules:
#'
#' 1. Rule 1
#' 2. Rule 2
#' 3. Rule 3
#'
#' @param x A numeric value, type `double`.
#' @param y A numeric value.
#'
#' @returns A numeric value.
#'
#' @examples
#' test_function(1, 2)
#' test_function(-5, 10)
subtract <- function(x, y) {
  x - y
}
